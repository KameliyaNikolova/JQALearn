package Homework1;

public class Task5 {
    public static void main(String[] args) {

        System.out.println("Числата от 1 до 100 във възходящ ред:");

        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
        System.out.println("Числата от 100 до 1 в низходящ ред:");

        for (int i = 100; i >= 1; i--) {
            System.out.print(i + " ");

        }
    }
}
